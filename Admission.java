    private void bafficherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bafficherActionPerformed

        try {
            dtm.setRowCount(0);
            dtm.setColumnCount(0);

            cp.rs = cp.ps.executeQuery("select * from patient");//actualisation

            dtm.addColumn("N° d'admission");
            dtm.addColumn("Nom");
            dtm.addColumn("Prenom");
            dtm.addColumn("Service");

            while (cp.rs.next()) {
                dtm.addRow(new Object[]{cp.rs.getObject("NumAdmission"),
                    cp.rs.getObject("Nom"),
                    cp.rs.getObject("Prenom"),
                    cp.rs.getObject("Service")});

            }
            tablep.setModel(dtm);

        } catch (SQLException ex) {
            Logger.getLogger(Admission.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_bafficherActionPerformed

