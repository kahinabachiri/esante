    private void tnumadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tnumadKeyTyped
         char car= evt.getKeyChar();
        if((car<'0' || car>'9' ) && tnumad.getText().contains(".")
            && (car!=(char)KeyEvent.VK_BACK_SPACE))
        {
            evt.consume();
            JOptionPane.showMessageDialog(null,"seulment numero","validation  "
                ,JOptionPane.INFORMATION_MESSAGE);
        } else if
        ((car<'0' ||car>'9')&&(car!='.')
            &&(car!=(char)KeyEvent.VK_BACK_SPACE)){
            evt.consume();
            JOptionPane.showMessageDialog(null,"SVP entrer seulement des numero","message de confirmation"
                ,JOptionPane.INFORMATION_MESSAGE); }


    }//GEN-LAST:event_tnumadKeyTyped

