    private void croleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_croleActionPerformed
       
        if (crole.getSelectedItem().equals("Administrateur")) {
            
            lmatricule.setText(matricule("ADM"));
            
        } else {
            
            if (crole.getSelectedItem().equals("Agent de saisie")) {
                lmatricule.setText(matricule("AGS"));
                
            } else {
                if (crole.getSelectedItem().equals("Medecin")) {
                    lmatricule.setText(matricule("MED"));
                    
                } else {
                    lmatricule.setText(matricule("CHS"));
                }
            }
        }
    }//GEN-LAST:event_croleActionPerformed

