    public Administrateur() {
        initComponents();
        setLocationRelativeTo(this);

        try {
            st=cn.connectbd().createStatement();
            rs=st.executeQuery("select Matricule,NomUtilisteur,MotDePasse,Nom,Prenom,Role from utilisateur");
            
            dtm.addColumn("Matricule");
            dtm.addColumn("nom d'utilisateur");
            dtm.addColumn("Mot de passe");
            dtm.addColumn("Nom");
            dtm.addColumn("Prenom");
            dtm.addColumn("Role");
            
            while (rs.next()) { 
               dtm.addRow(new Object[]{
                   rs.getObject("Matricule"),
                   rs.getObject("NomUtilisateur"),
                   rs.getObject("MotDePasse"),
                   rs.getObject("Nom"),
                   rs.getObject("Prenom"),
                   rs.getObject("Role")
                   
               });
                
            }
            tableuser.setModel(dtm);
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Administrateur.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
